// operadores de atribuição 
// são usados para atribuir valores a variáveis, adicionando ou sobrescrevendo

// ( = )
// atribui um valor a uma váriavel, sobrescrevendo o valor
let numero = 1;

// ( += )
// adiciona ao valor atual da variavel
numero += 2;

// é o mesmo que numero = numero + 2
// esse operador atribui o valor ao mesmo tempo que realiza a operação com o valor da variavel e com o valor desejado
// a mesma coisa acontece com os outros

// ( -= ) subtração
// ( *= ) multiplicação
// ( /= ) divisão
// ( %= ) modulo ou resto da divisão
// ( **= ) exponenciação
